    <h2>6. Submission Review Process</h2>

    <ul>
        <li>All submitted content goes through an admin review before going live on the platform</li>
        <li>Review timeframe: 3–7 working days (subject to volume)</li>
        <li>If content is rejected, the reason will be communicated to the creator</li>
        <li>Corrections can be made and content resubmitted after addressing the issue</li>
        <li>CG Chartbusters reserves the right to edit minor errors (spelling, formatting) before publishing</li>
        <li>Major inaccuracies will be sent back for creator correction</li>
    </ul>

