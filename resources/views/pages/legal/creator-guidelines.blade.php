    <h2>3. Content Quality Standards</h2>
    <p>All submitted content must meet the following quality requirements:</p>
    <h3>Accuracy</h3>

    <ul>
        <li>All information must be factually correct and up to date</li>
        <li>Correct spelling of names, titles, and places is mandatory</li>
        <li>Release year, genre, and language must be accurately tagged</li>
        <li>Do not submit duplicate or already-existing listings</li>
    </ul>

    <h3>Language & Tone</h3>

    <ul>
        <li>Content descriptions must be written professionally</li>
        <li>No abusive, offensive, or discriminatory language in any field</li>
        <li>Descriptions should be neutral and informative — not promotional or exaggerated</li>
    </ul>

    <h3>Completeness</h3>

    <ul>
        <li>Fill all mandatory fields before submission</li>
        <li>Incomplete submissions may be rejected or held for review</li>
        <li>Partial data can be updated after initial approval</li>
    </ul>

    <h2 class="leading-8">4. Prohibited Content — What You Must NOT Submit</h2>

        <h3>4.1 Piracy & Streaming Violations</h3>
        <p>The following are strictly prohibited and will result in immediate removal and account action:</p>
    <div class="danger">

        <ul>
            <li>❌ Watch Online links or embedded streaming players</li>
            <li>❌ Free Download or Direct Download links</li>
            <li>❌ Torrent links of any kind</li>
            <li>❌ Links to pirated or unauthorized copies of any content</li>
            <li>❌ File format references: MP4, MKV, HDRip, BluRay, WEB-DL, Dual Audio</li>
            <li>❌ Quality labels used for piracy: 720p, 1080p, 4K Download, HD Download</li>
        </ul>
    </div>

