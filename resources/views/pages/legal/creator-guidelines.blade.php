    <h2>3. Content Quality Standards</h2>

    <h3>Accuracy</h3>

    <ul>
        <li>All information must be correct and updated</li>
        <li>Use correct spelling for names and titles</li>
        <li>Avoid duplicate submissions</li>
    </ul>

    <h3>Language & Tone</h3>

    <ul>
        <li>Use professional language</li>
        <li>No abusive or offensive content</li>
        <li>Descriptions should be informative and neutral</li>
    </ul>

    <h3>Completeness</h3>

    <ul>
        <li>Fill all required fields</li>
        <li>Incomplete submissions may be rejected</li>
    </ul>

    <h2>4. Prohibited Content</h2>

    <div class="danger">
        <strong>Strictly Prohibited:</strong>

        <ul>
            <li>Watch Online links</li>
            <li>Free Download links</li>
            <li>Torrent links</li>
            <li>Pirated movie references</li>
            <li>720p / 1080p / HDRip / BluRay keywords</li>
        </ul>
    </div>

    <h3>Other Prohibited Submissions</h3>

    <ul>
        <li>Fake artist profiles</li>
        <li>Self-rating manipulation</li>
        <li>Unauthorized submissions</li>
        <li>Defamatory content</li>
        <li>Violence or illegal activity promotion</li>
    </ul>

