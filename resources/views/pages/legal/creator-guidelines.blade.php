<x-legal-page title="Content Submission Guidelines" lastUpdated="01 June 2026">
    <p>
        CG Chartbusters is a community-driven rating and discovery platform for
        Chhattisgarhi entertainment. These guidelines apply to all creators,
        artists, production houses, and representatives submitting content.
    </p>

    <h2>1. What Creators Can Submit</h2>

    <h3>1.1 Movie Listings</h3>

    <ul>
        <li>Title, release year, language, genre, synopsis</li>
        <li>Official cast and crew information</li>
        <li>Official poster image</li>
        <li>YouTube trailer link only</li>
        <li>Production house and distributor details</li>
    </ul>

    <h3>1.2 Song Listings</h3>

    <ul>
        <li>Song title and album/movie name</li>
        <li>Singer, lyricist, composer, label</li>
        <li>Official song thumbnail</li>
        <li>Official YouTube link only</li>
    </ul>

    <h3>1.3 Web Series Listings</h3>

    <ul>
        <li>Series title and release year</li>
        <li>Genre and language</li>
        <li>Episode count and platform info</li>
        <li>Official poster and trailer</li>
        <li>Cast and crew details</li>
    </ul>

    <h3>1.4 Artist / Creator Profiles</h3>

    <ul>
        <li>Full and professional name</li>
        <li>Profession details</li>
        <li>Profile photo</li>
        <li>Biography and career highlights</li>
        <li>Social media links</li>
        <li>Filmography and discography</li>
    </ul>

    <h3>1.5 Production House Profiles</h3>

    <ul>
        <li>Official registered name</li>
        <li>Logo and banner images</li>
        <li>Founded year and location</li>
        <li>Owner or head name</li>
        <li>Movies and songs produced</li>
        <li>Website and social media links</li>
    </ul>

