    <p>
        CG Chartbusters is a community-driven rating and discovery platform for Chhattisgarhi (Chhollywood) entertainment. As creators and artists contribute their data to our platform, it is essential that all submitted content meets our quality, accuracy, and legal standards
    </p>
    <p>
        These guidelines apply to all creators, artists, production houses, and representatives who submit or manage content on CGChartbusters.com.
    </p>
    <h2>1. What Creators Can Submit</h2>
    <p>The following types of content are accepted on CG Chartbusters: Movies, Songs, Web Series, Artist Profiles and Production House Profiles.</p>
    <h3>1.1 Movie Listings</h3>

    <ul>
        <li>Title, release year, language, genre, synopsis</li>
        <li>Official cast and crew information</li>
        <li>Official poster image (authorized promotional material only)</li>
        <li>Official trailer link (YouTube URL only — no direct video files)</li>
        <li>Production house name and distributor details</li>
    </ul>

    <h3>1.2 Song Listings</h3>

    <ul>
        <li>Song title and album/movie name release year</li>
        <li>Singer(s), lyricist, music composer, music label</li>
        <li>Official song cover/thumbnail image</li>
        <li>Official YouTube link only -- no audio file uploads</li>
    </ul>

    <h3>1.3 Web Series Listings</h3>

    <ul>
        <li>Series title, release year, genre, language</li>
        <li>Episode count and platform information</li>
        <li>Official poster and trailer (YouTube link only) </li>
        <li>Cast and crew details</li>
    </ul>

    <h3>1.4 Artist / Creator Profiles</h3>

    <ul>
        <li>Full and professional name (if different)</li>
        <li>Profession --Actor, Singer, Director, Lyricist etc.</li>
        <li>Profile photo (official/authorized image only)</li>
        <li>Biography and career highlights</li>
        <li>Social media handles and official links</li>
        <li>Filmography and discography</li>
    </ul>

    <h3>1.5 Production House Profiles</h3>

    <ul>
        <li>Production House ka official registered naam</li>
        <li>•	Logo image (Square 1:1 — 800x800px minimum, JPG/PNG, max 1MB)</li>
        <li>•	Banner image (Landscape 16:9 — 1280x720px minimum, JPG/PNG, max 2MB)</li>
        <li>•	Founded year aur location (City, State)</li>
        <li>•	Owner / Head ka naam (optional)</li>
        <li>•	Total movies, songs, web series produced</li>
        <li>•	Active years in industry</li>
        <li>•	Official website link (if available)</li>
        <li>•	YouTube channel link (if available)</li>
        <li>•	Social media handles</li>
        <li>•	Associated movies, songs, web series — jo already CGCB pe listed hain unse link hoga automatically</li>
    </ul>

