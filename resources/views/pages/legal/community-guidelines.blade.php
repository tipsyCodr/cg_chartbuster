    <p>CG Chartbusters is a respectful, inclusive, and community-driven platform dedicated to Chhattisgarhi (Chhollywood) entertainment including films, music, web series, and artists. To maintain a positive and trustworthy environment, all users must follow these guidelines.</p>

    <h2>1. Respectful Conduct</h2>
    <ul>
        <li>Do not use abusive, hateful, or discriminatory language of any kind</li>
        <li>Do not harass, bully, or threaten artists, creators, or other users</li>
        <li>Treat all community members with dignity and respect</li>
    </ul>

    <h2>2. Honest Reviews & Ratings</h2>
    <ul>
        <li>Reviews and ratings must reflect your genuine personal opinion</li>
        <li>Fake ratings, paid manipulation, coordinated review bombing, or spam activity is strictly prohibited</li>
        <li>Do not post reviews on behalf of others or for personal/commercial gain</li>
    </ul>

    <h2>3. No Illegal Content</h2>
    <ul>
        <li>Do not post or share pirated movie links or illegal downloads</li>
        <li>Do not promote or facilitate copyright infringement of any kind</li>
        <li>Do not upload copyrighted material without proper authorization</li>
    </ul>

    <h2>4. No Defamation</h2>
    <ul>
        <li>Avoid making false allegations against artists, creators, production houses, or businesses</li>
        <li>You are solely and legally responsible for every statement you publish on this platform</li>
        <li>Defamatory content will be removed immediately and may be reported to authorities</li>
    </ul>

    <h2>5. No Impersonation</h2>
    <ul>
        <li>Do not impersonate any artist, public figure, brand, or official representative of CG Chartbusters</li>
        <li>Creating fake profiles or misleading identities is prohibited</li>
    </ul>

    <h2>6. No Spam or Self-Promotion Abuse</h2>
    <ul>
        <li>Do not excessively self-promote in reviews, comments, or submissions</li>
        <li>Do not use the platform for unsolicited advertising or affiliate spam</li>
    </ul>

    <h2>7. Enforcement</h2>
    <p>Violation of these Community Guidelines may result in:</p>
    <ul>
        <li>Immediate content removal without notice</li>
        <li>Temporary account suspension</li>
        <li>Permanent account ban</li>
        <li>Legal action where applicable under Indian law</li>
    </ul>

    <p>CG Chartbusters reserves the right to moderate, remove, or restrict any content at its sole discretion.</p>


